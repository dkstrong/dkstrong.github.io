			<h3>General Rules:</h3>
			<ul>
			<li>Each player is dealt 5 cards which correspond to a card on the board (the board itself is made out of cards). The player can place chips on the board if they have the matching card in their hand.</li>
			<li>Two Eyed Jacks (Jack of Clubs and Jack of Diamonds) act as wild cards and can be placed anywhere on the board</li>
			<li>One Eyed Jacks (Jack of Spades and Jack of Hearts) are reverse wild cards, and can remove any of your opponent's chips from the table (unless that chip is already a part of a sequence)</li>
			<li>The 4 corners are wild and belong to all players and can be used as a chip in a sequence.</li>
			<li>The goal is to create a sequence of 5 chips either horizontally, vertically, or diagonally. The first player to create two sequences wins!</li>
			</ul>
			<h3>Game Modes:</h3>
